package com.demo.operators;

public class Arithmetic_operators {

	public static void main(String[] args) {
		int a, b=3, c=2;
		a=b+c;
		System.out.println("Value of 'a' after '+' Arithmetic operation is " +a);

		a=b-c;
		System.out.println("Value of 'a' after '+' Arithmetic operation is " +a);

		a=b*c;
		System.out.println("Value of 'a' after '*' Arithmetic operation is " +a);

		a=b/c;
		System.out.println("Value of 'a' after '/' Arithmetic operation is " +a);

		a=b%c;
		System.out.println("Value of 'a' after '%' Arithmetic operation is " +a);

		b++;
		System.out.println("Value of 'a' after '++' Arithmetic operation is " +b);

		c--;
		System.out.println("Value of 'a' after '--' Arithmetic operation is " +c);

	}


}
